"""Triangle quadrature for the BB element (Aeris BB Phase 2).

STANDALONE, pure stdlib, no numpy. Provides a quadrature rule on the
reference triangle T_ref = {(xi1,xi2): xi1>=0, xi2>=0, xi1+xi2<=1}
(area 1/2), plus a "domain iterator" that maps the reference rule onto
an arbitrary triangle.

## Choice of rule — collapsed (Duffy) Gauss-Legendre, NOT Xiao-Gimbutas

Ludwig (2018) adopts minimal SYMMETRIC rules (Xiao-Gimbutas). Those are
the product of a nonlinear moment-matching optimisation; their nodes
cannot be reconstructed by hand to working precision, and a mis-typed
digit would fail the monomial test with no oracle to repair it. So we
use the collapsed-coordinate (Duffy) construction over HARD-CODED 1D
Gauss-Legendre tables instead. That rule satisfies every hard
requirement and is verifiable from first principles:

  * positive weights      (Gauss-Legendre weights are positive; products
                           of positives stay positive)
  * strictly interior     (GL nodes lie in the open interval; the Duffy
                           map sends them into the open triangle)
  * exact to total degree 2(p-1)  (proven by the monomial test)
  * NOT generated by optimisation — only the standard 1D GL table, which
    cross-checks against closed forms (n<=3) and the monomial test
  * ports verbatim to C++ (the 1D table is the same literal constants)

Trade-off vs Xiao-Gimbutas: more points (n^2 vs the symmetric minimum)
and no S3 symmetry. Both are point-count / symmetry concerns, not
correctness. Dropping Ludwig's symmetric tables in later is a same-
interface swap gated by this module's monomial test.

## Duffy map

Square (u,v) in [0,1]^2 -> T_ref:  xi1 = u*(1-v),  xi2 = v,  |J| = (1-v).
  integral_Tref f dxi = integral_[0,1]^2 f(u(1-v), v) (1-v) du dv.
With n GL points per direction the integrand is degree (a) in u and
(a+b+1) in v for a monomial xi1^a xi2^b of total degree d=a+b, so n GL
points integrate exactly up to total degree 2n-2 (the v-direction, with
the extra Jacobian power, is the binding one). Hence for a target degree
D we take n = ceil((D+2)/2).

## Degree to request

For the KL shell STIFFNESS the membrane-binding exactness is 2(p-1).
The KL integrand is NON-polynomial even on flat elements (the sqrt in
the normal normalisation ||a3||), so monomial exactness is the rule's
unit test, NOT the physical acceptance criterion — that is "integration
error dominated by discretisation error" and only shows in the Phase-3
convergence study. Start at 2(p-1); the one-degree-higher rule is the
lever if a Phase-3 integration-error plateau ever appears. A consistent
MASS matrix would separately want 2p (not needed for stiffness).
"""
from __future__ import annotations

from math import ceil, factorial

# ---------------------------------------------------------------------------
