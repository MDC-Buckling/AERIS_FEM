_MAX_N = max(_GL)


def gauss_legendre_unit(n: int) -> tuple[list[float], list[float]]:
    """n-point Gauss-Legendre nodes/weights mapped to [0,1]
    (x' = (x+1)/2, w' = w/2). Exact for 1D polynomials up to degree 2n-1.
    sum(weights) == 1."""
    if n not in _GL:
        raise ValueError(f"no hard-coded GL table for n={n} (have 1..{_MAX_N})")
    nodes, weights = _GL[n]
    return ([0.5 * (x + 1.0) for x in nodes],
            [0.5 * w for w in weights])


def points_for_degree(degree: int) -> int:
    """1D point count n per direction so the Duffy rule is exact to the
    given TOTAL polynomial degree on the triangle."""
    if degree < 0:
        raise ValueError("degree must be >= 0")
    return max(1, ceil((degree + 2) / 2))


def quad_triangle(degree: int) -> list[tuple[float, float, float]]:
    """Reference-triangle quadrature exact to total polynomial `degree`.
    Returns [(xi1, xi2, weight), ...] with all weights > 0, all points
    strictly interior, and sum(weight) == area(T_ref) == 1/2."""
    n = points_for_degree(degree)
    if n > _MAX_N:
        raise ValueError(
            f"degree {degree} needs n={n} GL points; hard-coded table stops "
            f"at n={_MAX_N} (total degree {2 * _MAX_N - 2}). Add a 1D table.")
    u_nodes, u_w = gauss_legendre_unit(n)
    v_nodes, v_w = gauss_legendre_unit(n)
    pts = []
    for v, wv in zip(v_nodes, v_w):
        jac = 1.0 - v
        for u, wu in zip(u_nodes, u_w):
            xi1 = u * (1.0 - v)
            xi2 = v
            pts.append((xi1, xi2, wu * wv * jac))
    return pts


# ---------------------------------------------------------------------------
# Domain iterator: map the reference rule onto an arbitrary triangle.
# ---------------------------------------------------------------------------

def triangle_area(p0, p1, p2) -> float:
    """Unsigned area of the triangle (p0,p1,p2), points in R^2 or R^3."""
    a = [p1[i] - p0[i] for i in range(len(p0))]
    b = [p2[i] - p0[i] for i in range(len(p0))]
    if len(p0) == 2:
        cross = a[0] * b[1] - a[1] * b[0]
        return 0.5 * abs(cross)
    cx = a[1] * b[2] - a[2] * b[1]
    cy = a[2] * b[0] - a[0] * b[2]
    cz = a[0] * b[1] - a[1] * b[0]
    return 0.5 * (cx * cx + cy * cy + cz * cz) ** 0.5


def map_rule_to_triangle(p0, p1, p2, degree: int):
    """Affine-map the reference rule onto triangle (p0,p1,p2).

    Yields (point, weight) where point = p0 + xi1*(p1-p0) + xi2*(p2-p0)
    (in the embedding dimension of the vertices) and the weights scale by
    |det J| = 2*area, so sum(weight) == area(triangle) and the rule
    integrates polynomials of total degree `degree` (in the barycentric
    parametrisation) exactly. This is the standalone stand-in for the
    G+Smo makeDomainIterator contract, which is only validated against
    the assembler in Phase 3 — here it is checked against hand
    calculation only."""
    area = triangle_area(p0, p1, p2)
    scale = 2.0 * area                      # |det J| of ref(area 1/2)->target
    dim = len(p0)
    for (xi1, xi2, w) in quad_triangle(degree):
        pt = tuple(p0[i] + xi1 * (p1[i] - p0[i]) + xi2 * (p2[i] - p0[i])
                   for i in range(dim))
        yield pt, w * scale


# ---------------------------------------------------------------------------
# Exact reference integral of a monomial over T_ref (for the test module).
# ---------------------------------------------------------------------------

def monomial_integral_ref(a: int, b: int) -> float:
    """integral over T_ref of xi1^a xi2^b dxi = a! b! / (a+b+2)!."""
    return factorial(a) * factorial(b) / factorial(a + b + 2)
